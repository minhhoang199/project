    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {
        StringBuilder sb = new StringBuilder();
        if (txtID.getText().equals("")) {
            sb.append("Id can not be empty \n");
            txtID.setBackground(Color.red);
        } else {
            txtID.setBackground(Color.white);
        }
        if (txtName.getText().equals("")) {
            sb.append("Name can not be empty \n");
            txtName.setBackground(Color.red);
        } else {
            txtName.setBackground(Color.white);
        }
        if (txtPhone.getText().equals("")) {
            sb.append("Phone can not be empty \n");
            txtPhone.setBackground(Color.red);
        } else {
            txtPhone.setBackground(Color.white);
        }
        if (txtMail.getText().equals("")) {
            sb.append("Mail can not be empty \n");
            txtMail.setBackground(Color.red);
        } else {
            txtMail.setBackground(Color.white);
        }
        if (txtAddress.getText().equals("")) {
            sb.append("Address can not be empty \n");
            txtAddress.setBackground(Color.red);
        } else {
            txtAddress.setBackground(Color.white);
        }

        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb);
            return;
        }

        try {
            StudentDAO studentDAO = new StudentDAO();
            if (studentDAO.selectByID(txtID.getText()) != null) {
                JOptionPane.showMessageDialog(this, "ID has existed");
                txtID.setBackground(Color.red);
                return;
            } else {
                txtID.setBackground(Color.white);
                Student st = new Student();
                st.setId(txtID.getText());
                st.setName(txtName.getText());
                st.setEmail(txtMail.getText());
                st.setPhone(txtPhone.getText());
                st.setAddress(txtAddress.getText());
                st.setGender(rdbNam.isSelected() ? 1 : 0);
                studentDAO.insert(st);
                JOptionPane.showMessageDialog(this, "Student have id = " + txtID.getText() + " has been created");
                btnNewActionPerformed(evt);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            e.printStackTrace();
        }
    }

