    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {
        StringBuilder sb = new StringBuilder();
        if (txtID.getText().equals("")) {
            sb.append("Id need to be filled \n");
            txtID.setBackground(Color.red);
        } else {
            txtID.setBackground(Color.white);
        }
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb);
            return;
        }

        if (JOptionPane.showConfirmDialog(this, "Do you want to delete?") == JOptionPane.NO_OPTION){
            return;
        }
        try {
            StudentDAO studentDAO = new StudentDAO();
            Student st = studentDAO.selectByID(txtID.getText());
            if (st != null) {
                studentDAO.delete(txtID.getText());
                JOptionPane.showMessageDialog(this, "Student have id = " + txtID.getText() + " has been deleted");
                btnNewActionPerformed(evt);
            } else {
                JOptionPane.showMessageDialog(this, "Not found!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            e.printStackTrace();
        }
    }

